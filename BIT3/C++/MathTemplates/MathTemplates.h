#pragma once

template<typename T>T Add(T first, T second)
{
     return first+second;
}
template<typename T>T Sub(T first, T second)
{
     return first-second;
}
template<typename T>T Mul(T first, T second)
{
     return first*second;
}
template<typename T>T Div(T first, T second)
{
     return first/second;
}
